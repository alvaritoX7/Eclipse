package Objeto_03;

public class Yema {
	
	private String color;
	
	public Yema () {
		this.color = "amarillo";
	}
	
	public Yema(String color) {
		super();
		this.color = color;
	}
	
	public String getColor () {
		return this.color;
	}
	
	public void setColor (String color) {
		this.color = color;
	}
	
	
}
