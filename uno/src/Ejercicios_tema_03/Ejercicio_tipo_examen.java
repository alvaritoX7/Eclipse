package Ejercicios_tema_03;

public class Ejercicio_tipo_examen {

	public static void main(String[] args) {
		
		
		for(int decena = 0; decena <= 9; decena++) {
			for(int unidad = 0; unidad <= 9; unidad++) {
				if (unidad==1) {
					System.out.println("L");
				}
				else {
					System.out.println(decena + unidad);
				}
			}
		}
			
			
		
		
		
		
		
	}

}
